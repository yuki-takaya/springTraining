package com.example.demo.form;

public class Update {
	private String btnFlg;
	private String date;
	private String expense_item;
	private String amount;
	private String remark;
	
	private String id;

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getBtnFlg() {
		return btnFlg;
	}
	public void setBtnFlg(String btnFlg) {
		this.btnFlg = btnFlg;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getExpense_item() {
		return expense_item;
	}
	public void setExpense_item(String expense_item) {
		this.expense_item = expense_item;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
}
